package chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        // 비교 연산자
        System.out.println("1 == 1; " + (1 == 1));
        System.out.println("1 != 1; " + (1 != 1));
        System.out.println("1 != 0; " + (1 != 0));
        System.out.println("1 >= 1; " + (1 >= 1));
        System.out.println("2 >= 1; " + (2 >= 1));
        System.out.println("1 <= 1; " + (1 <= 1));
        System.out.println("1 <= 2; " + (1 <= 2));
        System.out.println("1 > 1; " + (1 > 1));
        System.out.println("2 > 1; " + (2 > 1));
        System.out.println("2 < 1; " + (2 < 1));
        System.out.println("1 < 2; " + (1 < 2));
    }
}
